import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const dist = path.join(root, 'dist');
const bootstrapPath = path.join(dist, 'ao3-helper.user.js');
const source = await readFile(bootstrapPath, 'utf8');
const starts = [...source.matchAll(/^System\.register\(/gm)].map((match) => match.index);
const importStart = source.lastIndexOf('\nSystem.import(');

if (!starts.length || importStart < starts.at(-1)) {
  throw new Error('Could not identify the SystemJS bundle boundaries');
}

const prefix = source.slice(0, starts[0]);
const suffix = source.slice(importStart);
const bootstrap = [];
const modules = [];
const panel = [];

for (let index = 0; index < starts.length; index += 1) {
  const start = starts[index];
  const end = index + 1 < starts.length ? starts[index + 1] : importStart;
  const chunk = source.slice(start, end);
  const name = chunk.match(/^System\.register\("([^"]+)"/)?.[1];
  if (!name) throw new Error('Could not identify a System.register chunk');
  if (name === './___monkey.entry.js' || name.startsWith('./dom-')) bootstrap.push(chunk);
  else if (name.startsWith('./panel-entry-')) panel.push(chunk);
  else modules.push(chunk);
}

if (!panel.length || !modules.length) {
  throw new Error(`Unexpected split result: panel=${panel.length}, modules=${modules.length}`);
}

const banner = (name) => `/* AO3 Helper physical ${name} bundle — generated by scripts/split-userscript.mjs */\n`;
const bootstrapSource = prefix + bootstrap.join('') + suffix;
const modulesSource = banner('modules') + modules.join('');
const panelSource = banner('panel') + panel.join('');
await Promise.all([
  writeFile(bootstrapPath, bootstrapSource, 'utf8'),
  writeFile(path.join(dist, 'ao3-helper.modules.js'), modulesSource, 'utf8'),
  writeFile(path.join(dist, 'ao3-helper.panel.js'), panelSource, 'utf8'),
]);

const kb = (text) => `${(Buffer.byteLength(text) / 1000).toFixed(2)} kB`;
console.log(`AO3 Helper split: bootstrap ${kb(bootstrapSource)}, modules ${kb(modulesSource)}, panel ${kb(panelSource)}`);
